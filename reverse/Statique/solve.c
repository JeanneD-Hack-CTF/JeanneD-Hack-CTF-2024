#include <stdlib.h>
#include <stdio.h>
#include <string.h>

int main(void) {
    for (int val = 0; val < 1073741824; ++val) {
      unsigned char encrypted[] = {
        0xc0, 0xeb, 0xe9, 0xee, 0xf1, 0xd8, 0xf8, 0xec, 0xfa, 0xe6, 0xf3, 0xce,
        0xda, 0xfc, 0xf9, 0xf0, 0xfb, 0xf6, 0xfc, 0xfc, 0xc5, 0xde, 0xf2, 0xfe,
        0xec, 0xe6, 0xd0, 0xd5, 0xcb, 0xcc, 0xca, 0xfa, 0xef, 0xd4, 0xf7, 0xfd,
        0xc2, 0xce, 0xf3, 0xef, 0xcb, 0xdc, 0xc4, 0xee, 0xf7, 0xdd, 0xd7, 0xc7,
        0xcf, 0xc7, 0xcc, 0xd0, 0xd5, 0xd5, 0x9d, 0xc0, 0xb4
      };
      // "decrypt the string"
      for (int i = 0; i < strlen((char *)encrypted); ++i) {
        encrypted[i] = encrypted[i] ^ (i + val);
      }

      // Check if the flag is correct (This part is optionnal and make the chall easier) 
      if (strncmp((char *)encrypted, "Flag", 4) == 0) {
        printf("Congratulations! You can validate with:\n%s\n", encrypted);
        return 0;
      }
    }
    return 0;
}
